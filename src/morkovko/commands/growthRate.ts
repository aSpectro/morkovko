import Command from './Command';
import { setEmbedAuthor } from './helpers';
import { AppService } from './../../app.service';
import e from 'express';

export class GRCommand extends Command {
  constructor(commandName: string) {
    super(commandName);
  }

  run(
    message: any,
    args: any,
    service: AppService,
    isSlash: boolean | undefined,
  ) {
    this.initCommand(message, args, service, isSlash, () => {
      const user = this.getUser();
      const { slotSpeedUpdate } = this.config.bot.economy;
      service.checkUser(user.id).then((res) => {
        if (res.status === 200 && res.player) {
          const player = res.player;
          const price = this.getPrice(player.slotsCount, slotSpeedUpdate, player.progressBonus);
          const count = this.getArgString('–∫–æ–ª-–≤–æ');
          if (count && player.points >= price * count && this.canBuy(player.carrotSize, 'slotSpeedUpdate', player.config.slotSpeedUpdate, count)) {
            player.config.slotSpeedUpdate += count;
            player.points -= price * count;
            service.savePlayer(player).then((resSave) => {
              if (resSave.status === 200) {
                this.embed.setDescription(
                  `–¢—ã —É–≤–µ–ª–∏—á–∏–ª –±–æ–Ω—É—Å —Å–∫–æ—Ä–æ—Å—Ç–∏ —Ä–æ—Å—Ç–∞ –º–æ—Ä–∫–æ–≤–∫–∏ –≤ –≥–æ—Ä—à–∫–∞—Ö. –¢–µ–∫—É—â–∏–π –±–æ–Ω—É—Å **${player.config.slotSpeedUpdate}%**`,
                );
                this.send({
                  embeds: [setEmbedAuthor(this.embed, user)],
                });
              } else {
                this.embed.setDescription(
                  `–ù–µ –ø–æ–ª—É—á–∏–ª–æ—Å—å –∫—É–ø–∏—Ç—å –±–æ–Ω—É—Å. –ü–æ–ø—Ä–æ–±—É–π –ø–æ–∑–∂–µ.`,
                );
                this.send({
                  embeds: [setEmbedAuthor(this.embed, user)],
                });
              }
            });
          } else {
            if (!count) {
              this.embed.setDescription(`–¢—ã –Ω–µ —É–∫–∞–∑–∞–ª –∫–æ–ª-–≤–æ!`);
            } else if (!this.canBuy(player.carrotSize, 'slotSpeedUpdate', player.config.slotSpeedUpdate, count)) {
              const acceptedCount = 50 - (player.config.slotSpeedUpdate + count);
              if (acceptedCount <= 0) {
                this.embed.setDescription(
                  `–¢—ã –Ω–µ –º–æ–∂–µ—à—å –∫—É–ø–∏—Ç—å —ç—Ç–æ—Ç –±–æ–Ω—É—Å! –°–µ–π—á–∞—Å —É —Ç–µ–±—è ${player.config.slotSpeedUpdate}/50.`,
                );
              } else if (player.carrotSize < this.config.bot.economy.shopRules['slotSpeedUpdate']) {
                this.embed.setDescription(`–¢–≤–æ—è –º–æ—Ä–∫–æ–≤–∫–∞ —Å–ª–∏—à–∫–æ–º –º–∞–ª–µ–Ω—å–∫–∞—è!`);
              } else {
                this.embed.setDescription(
                  `–¢—ã –º–æ–∂–µ—à—å –∫—É–ø–∏—Ç—å –Ω–µ –±–æ–ª—å—à–µ ${acceptedCount} –±–æ–Ω—É—Å–æ–≤! –°–µ–π—á–∞—Å —É —Ç–µ–±—è ${player.config.slotSpeedUpdate}/50.`,
                );
              }
            } else {
              this.embed.setDescription(
                `–¢–µ–±–µ –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç ${price - player.points}üî∏!`,
              );
            }
            this.send({
              embeds: [setEmbedAuthor(this.embed, user)],
            });
          }
        } else {
          this.replyNoUser(user);
        }
      });
    });
  }
}
